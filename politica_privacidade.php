    <div class="secao" id="quem-somos">
        <div class="secao-numero">01</div>
        <h2>Quem somos</h2>
        <p>A Risenglish é uma escola de inglês que oferece aulas, materiais de apoio e acompanhamento pedagógico por meio de plataforma digital própria. Esta política descreve como tratamos os dados pessoais de alunos, professores e demais usuários da plataforma, em conformidade com a Lei Geral de Proteção de Dados Pessoais (LGPD — Lei nº 13.709/2018).</p>
        <p>Ao utilizar a plataforma, você confirma que leu e compreendeu esta política.</p>
    </div>

    <!-- 2 -->
    <div class="secao" id="dados-coletados">
        <div class="secao-numero">02</div>
        <h2>Dados que coletamos</h2>
        <p>Coletamos apenas os dados necessários para o funcionamento da plataforma e para a prestação dos serviços educacionais. São eles:</p>
        <ul>
            <li><strong>Dados de cadastro:</strong> nome completo, endereço de e-mail e senha (armazenada de forma criptografada).</li>
            <li><strong>Dados de contato:</strong> número de celular, quando fornecido.</li>
            <li><strong>Documentos e contratos:</strong> arquivos enviados pelo administrador da plataforma, que podem conter CPF, endereço, e outras informações pessoais dos alunos.</li>
            <li><strong>Dados de acesso:</strong> registros de login com data, horário e identificação parcial do endereço IP (os dois últimos grupos do IP são sempre omitidos).</li>
            <li><strong>Informações pedagógicas:</strong> anotações, agendamentos de aulas, comentários e conteúdos registrados na plataforma.</li>
        </ul>
        <div class="destaque">
            <strong>Não coletamos</strong> dados de pagamento diretamente — transações financeiras são tratadas por sistemas externos quando aplicável. Também não utilizamos ferramentas de rastreamento como Google Analytics.
        </div>
    </div>

    <!-- 3 -->
    <div class="secao" id="finalidade">
        <div class="secao-numero">03</div>
        <h2>Como usamos os dados</h2>
        <p>Os dados coletados são utilizados exclusivamente para as seguintes finalidades:</p>
        <ul>
            <li>Autenticar e gerenciar o acesso à plataforma;</li>
            <li>Organizar e registrar as aulas, conteúdos e atividades pedagógicas;</li>
            <li>Armazenar e disponibilizar contratos e documentos relacionados à matrícula;</li>
            <li>Registrar logs de acesso para fins de segurança e auditoria;</li>
            <li>Comunicar informações relevantes sobre as aulas e a plataforma.</li>
        </ul>
        <p>Nenhum dado pessoal é compartilhado com terceiros para fins comerciais ou publicitários.</p>
    </div>

    <!-- 4 -->
    <div class="secao" id="menores">
        <div class="secao-numero">04</div>
        <h2>Menores de idade</h2>
        <p>A plataforma Risenglish pode ser utilizada por alunos menores de 18 anos. Nestes casos, exigimos que o cadastro e a aceitação desta política sejam realizados pelo responsável legal do menor.</p>
        <p>Os dados de menores de idade recebem o mesmo nível de proteção dos demais usuários, e o acesso aos seus documentos e informações é restrito ao administrador da plataforma e ao próprio aluno (ou responsável).</p>
        <div class="destaque">
            <strong>Responsáveis legais</strong> podem solicitar a qualquer momento o acesso, correção ou exclusão dos dados do menor sob sua responsabilidade, conforme descrito na seção de direitos abaixo.
        </div>
    </div>

    <!-- 5 -->
    <div class="secao" id="acesso">
        <div class="secao-numero">05</div>
        <h2>Quem acessa seus dados</h2>
        <p>O acesso aos dados pessoais na plataforma é controlado por perfis de usuário com permissões distintas:</p>
        <ul>
            <li><strong>Administrador:</strong> acesso completo a todos os dados, documentos e registros da plataforma;</li>
            <li><strong>Professores:</strong> acesso aos dados pedagógicos e documentos dos alunos sob sua responsabilidade;</li>
            <li><strong>Alunos:</strong> acesso apenas aos próprios dados, aulas, anotações e documentos.</li>
        </ul>
        <p>Nenhum usuário tem acesso aos dados de outro usuário fora do que é necessário para a prestação do serviço educacional.</p>
    </div>

    <!-- 6 -->
    <div class="secao" id="armazenamento">
        <div class="secao-numero">06</div>
        <h2>Armazenamento e segurança</h2>
        <p>Os dados são armazenados em servidores seguros e protegidos pelas seguintes medidas técnicas:</p>
        <ul>
            <li>Comunicação criptografada via HTTPS em todas as páginas;</li>
            <li>Senhas armazenadas com criptografia forte (bcrypt), nunca em texto simples;</li>
            <li>Cookies de sessão configurados com proteções contra interceptação e falsificação;</li>
            <li>Acesso aos arquivos protegido por autenticação — documentos não são acessíveis por URL direta;</li>
            <li>Endereços IP registrados nos logs de forma parcialmente ofuscada, preservando apenas os dois primeiros grupos;</li>
            <li>Proteções contra ataques comuns como SQL Injection, XSS e CSRF.</li>
        </ul>
    </div>

    <!-- 7 -->
    <div class="secao" id="retencao">
        <div class="secao-numero">07</div>
        <h2>Retenção de dados</h2>
        <p>Os dados são mantidos enquanto o relacionamento com o aluno ou professor estiver ativo na plataforma. Após o encerramento, os dados podem ser mantidos por até 6 meses para fins de auditoria e cumprimento de obrigações legais, sendo então eliminados.</p>
        <ul>
            <li><strong>Logs de acesso:</strong> mantidos por 6 meses, excluídos automaticamente após esse período;</li>
            <li><strong>Documentos e contratos:</strong> mantidos enquanto o aluno estiver ativo. Podem ser removidos a pedido do titular;</li>
            <li><strong>Dados de cadastro:</strong> removidos mediante solicitação formal, desde que não haja obrigação legal de retenção.</li>
        </ul>
    </div>

    <!-- 8 -->
    <div class="secao" id="direitos">
        <div class="secao-numero">08</div>
        <h2>Seus direitos</h2>
        <p>Nos termos da LGPD, você tem os seguintes direitos em relação aos seus dados pessoais:</p>
        <ul>
            <li><strong>Acesso:</strong> solicitar uma cópia dos dados que temos sobre você;</li>
            <li><strong>Correção:</strong> solicitar a atualização de dados incorretos ou desatualizados;</li>
            <li><strong>Eliminação:</strong> solicitar a exclusão dos seus dados, quando aplicável;</li>
            <li><strong>Portabilidade:</strong> solicitar seus dados em formato estruturado;</li>
            <li><strong>Revogação de consentimento:</strong> retirar seu consentimento a qualquer momento, sem prejuízo dos serviços prestados até então;</li>
            <li><strong>Informação:</strong> ser informado sobre com quem seus dados são compartilhados.</li>
        </ul>
        <p>Para exercer qualquer um desses direitos, entre em contato pelo e-mail indicado na seção abaixo. Responderemos em até 15 dias úteis.</p>
    </div>

    <!-- 9 -->
    <div class="secao" id="cookies">
        <div class="secao-numero">09</div>
        <h2>Cookies e sessões</h2>
        <p>A plataforma utiliza apenas cookies estritamente necessários para o funcionamento do sistema de autenticação. Esses cookies:</p>
        <ul>
            <li>São criados exclusivamente após o login na plataforma;</li>
            <li>Não rastreiam sua navegação em outros sites;</li>
            <li>São automaticamente removidos ao fechar o navegador;</li>
            <li>São configurados com proteções de segurança (HttpOnly, Secure, SameSite).</li>
        </ul>
        <p>Por utilizarmos apenas cookies essenciais, não é necessário exibir um banner de consentimento de cookies.</p>
    </div>

